            <p><span class="bld">A short desription on how to use the DVD</span><br />This DVD provides high-resolution scans of the entire collection of <span class="bld">Vedanta Kesari,</span> which can be accessed by clicking on <span class="bld"><a href="volumes.php">Volumes</a></span> link.</p>
            <ul class="about_ul">
                <li>List of all Article titles sorted alphabetically (click on <span class="bld"><a href="articles.php">Articles</a></span> link)</li>
                <li>Articles of a particular author (click on <span class="bld"><a href="authors.php">Authors</a></span> link)</li>
                <li>Table of Contents of each issue (click on <span class="bld"><a href="volumes.php">Volumes</a></span> link and select an issue)</li>
                <li>To Search for articles or words (click on <span class="bld"><a href="search.php">Search</a></span> link)</li>
                <li>Table of contents contain hyperlinks to articles, their authors and the year/month in which they appear. Clicking on a particular article title will open the article in DjVu format. Clicking on the author's name will display the list of articles of that particular author, and these articles can then be accessed by clicking on them. Clicking on the year/month will open the table of contents of that particular issue.</li>
            </ul>
            <p><span class="bld">How to search for an article.</span><br />To search for an article click on the <span class="bld"><a href="search.php">Search</a></span> link and enter details such as title of the article and/or its author and/or the period in which the article might have appeared. On clicking the Search button, search result pertaining to the set of values entered, will be displayed. After opening an article, user can search through it using the plug-in search facility. (Press 'F' and enter the search term. On clicking Find next, the search term, if found will be highlighted in the article.)</p>
            <p><span class="bld">How to search for a word.</span><br />To search for a word or phrase in the entire archive click on the search link and enter the word in the Text field. This facility can be used along with the facility provided to search for an article.</p>
            <p><span class="bld">How to print a page.</span><br />Open the required page of the archive and click on File > Print. Using the Page Setup tab, printing can be customized. To print a portion of a page, export the page into a .BMP image file (Right click on the page and click on Export). The image can then be cropped  and printed using any image editing tool. To print a picture in the Pictures gallery, click on the thumbnail of the picture. After a full size image is obtained on the screen, click on the printer icon at the bottom of the image.</p>
            <p><span class="bld">Troubleshooting FAQ</span></p>
            <p>
                <span class="bld">Q.</span> AutoRun fails to start the archive.<br />
                <span class="bld">A.</span> Click on the file 'Vedanta_Kesari' in the DVD drive.
            </p>
            <p>
                <span class="bld">Q.</span> A runtime error is encountered.<br />
                <span class="bld">A.</span> Copy the entire folder 'FirefoxPortable' situated in the DVD drive to C:\ drive of your computer. Then click on the file 'vk' in the DVD drive.
            </p>
            <p>
                <span class="bld">Q.</span> I am able to browse through the Author-Article pages but I am unable to open any page of the archive(DjVu file).<br />
                <span class="bld">A.</span> A DjVu plugin-version incompatibility might have occurred. Please install the djvu plugin given in the DVD (Software/DjVuPlugin/djvuctrl-6.1.4-en-r27999.msi)
            </p>
            <p>
                <span class="bld">Q.</span> The DVD tray is locked and I am not able to remove the DVD.<br />
                <span class="bld">A.</span> The archive might still be active. Right-click on the tray icon 'Vedanta Kesari Archive' and click on 'Close Vedanta Kesari Archive' and then remove the DVD.
            </p>
            <p>For further queries and a more elaborate FAQ please visit <span class="bld"><a href="http://www.srirangadigital.com/" target="_blank">http://www.srirangadigital.com/</a></span></p>
